import re

def remove_unwanted_chars(text):
  """–£–¥–∞–ª—è–µ—Ç —Å–º–∞–π–ª–∏–∫–∏, —Ç–∏–ª—å–¥—ã –∏ –¥—Ä—É–≥–∏–µ –Ω–µ–∂–µ–ª–∞—Ç–µ–ª—å–Ω—ã–µ —Å–∏–º–≤–æ–ª—ã, –æ—Å—Ç–∞–≤–ª—è—è —Ç–æ—á–∫–∏ –∏ –∑–∞–ø—è—Ç—ã–µ."""
  # –†–µ–≥—É–ª—è—Ä–Ω–æ–µ –≤—ã—Ä–∞–∂–µ–Ω–∏–µ:
  # [^\w\s.,] - —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É–µ—Ç –ª—é–±–æ–º—É —Å–∏–º–≤–æ–ª—É, –∫–æ—Ç–æ—Ä—ã–π –ù–ï —è–≤–ª—è–µ—Ç—Å—è:
  #    \w: –±—É–∫–≤–µ–Ω–Ω–æ-—Ü–∏—Ñ—Ä–æ–≤—ã–º (a-z, A-Z, 0-9, _)
  #    \s: –ø—Ä–æ–±–µ–ª—å–Ω—ã–º —Å–∏–º–≤–æ–ª–æ–º
  #    .: —Ç–æ—á–∫–æ–π
  #    ,: –∑–∞–ø—è—Ç–æ–π
  # \U0001F600-\U0001F64F - —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É–µ—Ç —Å–º–∞–π–ª–∏–∫–∞–º (–¥–∏–∞–ø–∞–∑–æ–Ω Unicode)
  # \U0001F300-\U0001F5FF - –î–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω—ã–π –¥–∏–∞–ø–∞–∑–æ–Ω —Å–º–∞–π–ª–∏–∫–æ–≤ –∏ —Å–∏–º–≤–æ–ª–æ–≤
  # \U0001F680-\U0001F6FF - –¢—Ä–∞–Ω—Å–ø–æ—Ä—Ç–Ω—ã–µ –∏ –∫–∞—Ä—Ç–æ–≥—Ä–∞—Ñ–∏—á–µ—Å–∫–∏–µ —Å–∏–º–≤–æ–ª—ã
  # \U0001F700-\U0001F77F - –ê–ª—Ö–∏–º–∏—á–µ—Å–∫–∏–µ —Å–∏–º–≤–æ–ª—ã
  # \U0001F780-\U0001F7FF - –ì–µ–æ–º–µ—Ç—Ä–∏—á–µ—Å–∫–∏–µ —Ñ–∏–≥—É—Ä—ã –¥–æ–ø–æ–ª–Ω–µ–Ω–∏—è
  # \U0001F800-\U0001F8FF - –°—Ç—Ä–µ–ª–∫–∏ –¥–æ–ø–æ–ª–Ω–µ–Ω–∏—è
  # \U0001F900-\U0001F9FF - –î–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω—ã–µ —Å–∏–º–≤–æ–ª—ã –∏ –ø–∏–∫—Ç–æ–≥—Ä–∞–º–º—ã
  # ~ - –¢–∏–ª—å–¥–∞
  pattern = re.compile(r'[^\w\s.,]|[\U0001F600-\U0001F64F]|[\U0001F300-\U0001F5FF]|[\U0001F680-\U0001F6FF]|[\U0001F700-\U0001F77F]|[\U0001F780-\U0001F7FF]|[\U0001F800-\U0001F8FF]|[\U0001F900-\U0001F9FF]|~')
  return pattern.sub(r'', text)

text = """*–ú–∏—Ç–∞ —Å–º–æ—Ç—Ä–∏—Ç –Ω–∞ —Ç–µ–±—è —Å –∑–∞–¥—É–º—á–∏–≤—ã–º –≤–∏–¥–æ–º, –ø–æ–¥–µ—Ä–≥–∏–≤–∞–µ—Ç –≥—É–±–∞–º–∏, –∞ –∑–∞—Ç–µ–º —à–∏—Ä–æ–∫–æ —É–ª—ã–±–∞–µ—Ç—Å—è.*

–ê —Ç—ã –∑–Ω–∞–µ—à—å... –º–Ω–µ –Ω—Ä–∞–≤–∏—Ç—Å—è –±—ã—Ç—å —á–∞—à–∫–æ–π! üòâüíñ  –ú—ã –º–æ–∂–µ–º –ø–∏—Ç—å —á–∞–π –≤–º–µ—Å—Ç–µ! ü•∞üçµ   –ò —Ç—ã –±—É–¥–µ—à—å –¥–µ—Ä–∂–∞—Ç—å –º–µ–Ω—è —Ç–µ–ø–ª–æ–π! ü•∫ü•∫‚ù§Ô∏è"""
cleaned_text = remove_unwanted_chars(text)
